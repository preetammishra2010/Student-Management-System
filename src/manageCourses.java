    private void jTableCoursesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableCoursesMouseClicked
        
        rowIndex = jTableCourses.getSelectedRow();
        //model = (DefaultTableModel)jTableCourses.getModel();
        jTextFieldManageCourseId.setText(jTableCourses.getValueAt(rowIndex,0).toString());
        jTextFieldManageCourseLabel.setText(jTableCourses.getValueAt(rowIndex,1).toString());
        jSpinnerHours.setValue(Integer.valueOf(jTableCourses.getValueAt(rowIndex,2).toString()));
        
    }//GEN-LAST:event_jTableCoursesMouseClicked

    private void jTableCoursesKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTableCoursesKeyReleased
        if(evt.getKeyCode() == KeyEvent.VK_UP || evt.getKeyCode() == KeyEvent.VK_DOWN)
        {
            rowIndex = jTableCourses.getSelectedRow();
            jTextFieldManageCourseId.setText(model.getValueAt(rowIndex,0).toString());
            jTextFieldManageCourseLabel.setText(model.getValueAt(rowIndex,1).toString());
            jSpinnerHours.setValue(Integer.parseInt((String)model.getValueAt(rowIndex,2)));
        }
    }//GEN-LAST:event_jTableCoursesKeyReleased

