    private void jButtonEditCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditCourseActionPerformed
        if(jTextFieldManageCourseLabel.getText().equals(""))
        {
           JOptionPane.showMessageDialog(null,"Couse not selected");
        }
        else
        {
        int id = Integer.valueOf(jTextFieldManageCourseId.getText());
         String  label = jTextFieldManageCourseLabel.getText();
         int hours = Integer.valueOf(jSpinnerHours.getValue().toString());
         c.insertUpdateDeleteCourse('u',id,label,hours);
         MainForm.LabeCourseCount.setText("Course Count = " + Integer.toString(MyFunction.countData("Courses")));
         manageCourses.jTableCourses.setModel(new DefaultTableModel(null,new Object[]{"Id","Coursename","Hours"}));
         c.fillCourseJTable(manageCourses.jTableCourses);
         JOptionPane.showMessageDialog(null,"Course Edited");
        }

    }//GEN-LAST:event_jButtonEditCourseActionPerformed

    private void jButtonDeleteCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteCourseActionPerformed
        // TODO add your handling code here:
        if(jTextFieldManageCourseId.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Course not selected");
        }
        else
        {
            int id = Integer.valueOf(jTextFieldManageCourseId.getText());
            c.insertUpdateDeleteCourse('d', id, null, null);
            manageCourses.jTableCourses.setModel(new DefaultTableModel(null,new Object[]{"Id","Coursename","Hours"}));
            MainForm.LabeCourseCount.setText("Course Count = " + Integer.toString(MyFunction.countData("Courses")));
            c.fillCourseJTable(manageCourses.jTableCourses);
            jTextFieldManageCourseId.setText("");
            jTextFieldManageCourseLabel.setText("");
            jSpinnerHours.setValue(3);
        }
    }//GEN-LAST:event_jButtonDeleteCourseActionPerformed

